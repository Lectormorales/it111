public class MyConcatenation {

    public static void main(String[]args){
        String color = "GREEN";
        System.out.println("My favorite color is "+color);

        int money = 4_000_000;
        System.out.println("I hit the jackpot and won $"+money+" dollars!");
        System.out.println(color+" "+money);
        System.out.println("This is the statement of my favorite color which is "+color+" and I also won $"+money+" dollars!");

    }
}
