public class MyDataTypes {
    public static void main(String[] args) {

        short number10 = 32_000;
        int number20 = 2_000_000;
        long number30 = 3_000_000L;
        float number40 = 30.5F;
        double number50 = 50.45637377;


        System.out.println(number10);
        System.out.println(number20);
        System.out.println(number30);
        System.out.println(number40);
        System.out.println(number50);


        boolean dry = false;
        if (dry) {
            System.out.println("Time to have a snack or two outside!");
        } else {
            System.out.println("Time to stay!");
        }

        char best = 'A';
        System.out.println("best");


    }

}
